public class JumlahKodeASCII {
    
    public static void main(String[] args) {
        
        char karakterA = 'A';
        char karakterB = 'B';
        
        int kodeA = (int) karakterA;
        int kodeB = (int) karakterB;
        
        int jumlah = kodeA + kodeB;
        
        System.out.println("Jumlah kode ASCII dari karakter A dan B adalah: " + kodeA + " + " + kodeB + " = " + jumlah);
        
    }
    
}
